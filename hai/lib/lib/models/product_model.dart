class Product {
  final String id;
  final String name;
  final String description;
  final double price;
  final String image;
  final String instructor;
  final double rating;
  final int students;

const Product({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.image,
    required this.instructor,
    required this.rating,
    required this.students,
  });
}